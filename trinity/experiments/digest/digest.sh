#!/usr/bin/env bash
set -euo pipefail

# Google Drive Daily Digest
# Fetches recent notes from specified folders and consolidates them.

# Folder IDs (hard-coded)
THOUGHTS_FOLDER="1rGAqmFzgo3bZS8luFBQAbGUVNXfN5q83"
SCHOOL_FOLDER="1-WUokNLwKe9hpIyNyzNQY6fTqxOl7G3l"
NETIC_FOLDER="1js07mgZxVRzOeCE2BAevtA1ICraFIMoY"

OUTPUT_DIR="$HOME/.openclaw/workspace/trinity/digests"
mkdir -p "$OUTPUT_DIR"

TODAY=$(date +%Y-%m-%d)
OUTPUT_FILE="$OUTPUT_DIR/$TODAY.md"

# Create digest header
cat > "$OUTPUT_FILE" <<EOF
# Daily Digest — $TODAY

**Sources:** thoughts, school, netic

---

EOF

# Function to get the most recent file (markdown or Google Doc) from a folder
get_latest_file() {
    local folder_id=$1
    gws drive files list --params "{\"q\": \"'$folder_id' in parents\", \"orderBy\": \"modifiedTime desc\", \"pageSize\": 10, \"fields\": \"files(id,name,mimeType,modifiedTime)\"}" 2>/dev/null |
    jq -r '.files[] | select(.mimeType | test("text/markdown|application/vnd.google-apps.document")) | "\(.modifiedTime) \(.id) \(.mimeType) \(.name)"' |
    head -1
}

# Function to fetch content of a file by ID (supports markdown and Google Docs)
fetch_content() {
    local file_id=$1
    local mime_type=$2
    # Create a temporary file in the current directory (allowed by gws)
    local tmp_out
    tmp_out=$(mktemp "gws_${file_id}_XXXXXX")
    # Ensure cleanup
    trap 'rm -f "$tmp_out"' RETURN

    if [[ "$mime_type" == "text/markdown" ]]; then
        # Suppress stdout/stderr from gws; only file content is saved
        gws drive files get --params "{\"fileId\": \"$file_id\", \"alt\": \"media\"}" --output "$tmp_out" > /dev/null 2>&1
    elif [[ "$mime_type" == "application/vnd.google-apps.document" ]]; then
        gws drive files export --params "{\"fileId\": \"$file_id\", \"mimeType\": \"text/plain\"}" --output "$tmp_out" > /dev/null 2>&1
    else
        echo "Unsupported mime: $mime_type" >&2
        return 1
    fi

    cat "$tmp_out"
}

# Process a section
process_section() {
    local section_name=$1
    local folder_id=$2
    local max_lines=$3

    echo "## $section_name" >> "$OUTPUT_FILE"
    echo "" >> "$OUTPUT_FILE"

    local info
    info=$(get_latest_file "$folder_id")
    if [[ -n "$info" ]]; then
        read -r time fid mime name <<<"$info"
        local formatted_time
        formatted_time=$(date -d "$time" +%Y-%m-%d\ %H:%M 2>/dev/null || echo "$time")
        echo "### $name (updated $formatted_time)" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        # Pipe through head; ignore SIGPIPE errors
        fetch_content "$fid" "$mime" 2>/dev/null | head -"$max_lines" >> "$OUTPUT_FILE" || true
        echo "" >> "$OUTPUT_FILE"
    else
        echo "*(no recent markdown or doc files found)*" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
    fi
}

# Process each section
process_section "Thoughts" "$THOUGHTS_FOLDER" 50
process_section "School" "$SCHOOL_FOLDER" 50
process_section "Netic" "$NETIC_FOLDER" 80

# Footer
echo "---" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"
echo "*Generated by Trinity Daily Digest on $(date -u +%Y-%m-%d\ %H:%M\ UTC)*" >> "$OUTPUT_FILE"

echo "Digest created: $OUTPUT_FILE"
